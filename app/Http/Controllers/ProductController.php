    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $file = public_path('productos.json');
        $json = file_get_contents($file);
        $data = json_decode($json, true);

        foreach ($data as $productData) {
            $product = new Product;
            $product->name = $productData['name'];
            $product->categoria = $productData['categoria'];
            $product->marca = $productData['marca'];
            $product->codigoColor = $productData['codigoColor'];
            $product->precio = $productData['precio'];
            $product->stock = $productData['stock'];
            $product->save();
        }

        $products = Product::all();

        return redirect()->route('product.index', compact('products'));
    }

